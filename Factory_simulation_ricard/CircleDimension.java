package simulateur_robot;

public class CircleDimension extends Dimension {
	private int radius;
	
	

	public CircleDimension(int radius) {
		super();
		this.radius = radius;
	}

	public int getRadius() {
		return radius;
	}

	public void setRadius(int radius) {
		this.radius = radius;
	}
	

}
